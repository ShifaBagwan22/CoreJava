//NESTED IF ELSE DEMO
package com.basic;
public class Condition3 
{
	public static void main(String[] args) 
	{
		int no=12;
		if(no>=0)
		{
			if(no==0)
			{
				System.out.println("YOUR NO IS ZERO...");
			}
			else
			{
				System.out.println("YOUR NO IS POSITIVE...");
			}
		}
		else
		{
			System.out.println("YOUR NO IS NEGATIVE...");
		}
	}

}
